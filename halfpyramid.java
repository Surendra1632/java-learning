public class halfpyramid {
    public static void main(String args[]){
        int n=4;
      //  int number=0;
        for(int i=1;i<=n;i++){
            for(int j=1;j<=i;j++){
                System.out.print(j);
                //number++; 
            }
            System.out.println();
        }
    }
    
}
